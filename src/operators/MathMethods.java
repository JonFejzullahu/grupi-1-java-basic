package operators;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.abs(-5));
        System.out.println(Math.abs(5));

        System.out.println(Math.max(100, 1000));
        System.out.println(Math.max(2000, 200));

        System.out.println(Math.min(100, 1000));
        System.out.println(Math.min(2000, 200));

        System.out.println(Math.pow(2, 10));  // 2^10 = 1024
        System.out.println(Math.pow(3, 3)); // 3^3 = 27

        System.out.println(Math.sqrt(4)); // sqrt = square root - rrenja katrore
        System.out.println(Math.sqrt(100));

        System.out.println(Math.round(2.7));

        System.out.println(Math.floor(2.7)); // floor
        System.out.println(Math.ceil(2.7)); // ceil

        System.out.println(Math.log(4)); // log i exponentes Math.lg
        System.out.println(Math.sin(300)); // numri brenda eshte ne radian

        System.out.println(Math.E);
        System.out.println(Math.PI);


    }
}
